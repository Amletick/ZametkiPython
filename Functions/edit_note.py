def edit_note(manager):
    note_id = int(input("Введите ID заметки для редактирования: "))
    title = input("Введите новый заголовок заметки (оставьте пустым, если не хотите изменять): ")
    message = input("Введите новый текст заметки (оставьте пустым, если не хотите изменять): ")
    if manager.edit_note(note_id, title, message):
        print("Заметка успешно отредактирована!")
    else:
        print("Заметка с указанным ID не найдена.")

